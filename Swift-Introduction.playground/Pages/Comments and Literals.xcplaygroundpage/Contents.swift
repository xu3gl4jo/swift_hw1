/*:

 # Comments

 Comments are content for developers, but not for the Swift compiler.

 - Comment is used to denote that the reason or information about why we write
 these lines of code.
 - Comments usually contains about the reason but not how to implement this.
 _Use proper variable names to make your code self-explained._

 */

//: We put double slash (`//`) in front of a **single-line comment**. Like this:
// This is a single-line comment.

//: And use `/*` and `*/` to wrap **multi-line comment** like:
/*
 * This is a multi-line comment.
 */

//: In Xcode, we use `command+/` to comment or uncomment selected lines.
//:
//: --------------------------------------------------------------------------------------------------------------------

/*:

 # Literals

 - A literal value is a value that appears directly in your source code.
 - Swift uses double quote as string literals.

 */
42  // This is an integer literal.
3.1415926  // This is a literal of float numbers.
"Hello World!"  // This is a string literal.
true  // This is one of the only two boolean literals. It means "true", "correct", or "valid".
false  // This is the another boolean literal, which means "false", "incorrect", or "invalid".

